var updatePageValues = function () {
    $(".chp").on("select2:select", function (e) {
        var chp = $(".chp").select2('data')[0].id;
        var pageList = getPagesInChapter()[chp];
        var pageDescriptions = getPageDescription();
        $('.pg').empty();

        var data = [];
        pageList.forEach(function (page) {
            data.push({
                id: page,
                text: pageDescriptions[page]
            })
        });
        $(".pg").select2({
            data: data,
            placeholder: "Choose a Page",
            width: "auto",
            dropdownPosition: "below",
        });
    });
}

var updatePageEntries = function () {
    var page = $(".pg").select2("data")[0].id;
    $.ajax({ url: `/Search/FaulknerEntries?page=${page}`, type: 'GET' }).done(async function (result) {
        console.log(result);
        /*
         * Hieroglyphs
         * Transliteration
         * Gardiner Signs
         * Translations
         */
        result.forEach(function (item) {
            var row = document.createElement("tr");

            var r1 = document.createElement("td");
            r1.innerHTML = `<canvas class="res">${item.Res != null ? item.Res : item.ManuelDeCodage}</canvas>`;
            row.appendChild(r1);

            var r2 = document.createElement("td");
            r2.innerText = item.Transliteration;
            row.appendChild(r2);

            var r3 = document.createElement("td");
            r3.innerText = item.GardinerSigns;
            row.appendChild(r3);

            var r4 = document.createElement("td");
            r4.innerHTML = item.Translations.translation;
            row.appendChild(r4);

            $("#resultBody").append(row);

            $("#hiddenTable").show();
        });
        await new Promise(_ => ResWeb());
    });
}

